def playerhandvalue(playercard1value,playercard2alue):
    valueplayer = playercard1value + playercard2alue
    return valueplayer

def dealerhandvalue(dealercard1value,dealercard2alue):
    valuedealer = dealercard1value + dealercard2alue
    return valuedealer

def hitvalue(bvalues,valueplayer):
    valueplayer = valueplayer + bvalues
    return valueplayer